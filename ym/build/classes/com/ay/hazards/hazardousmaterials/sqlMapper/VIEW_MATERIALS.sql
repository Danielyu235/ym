--危险物质的总视图
CREATE VIEW VIEW_MATERIALS AS 
(
SELECT
TB_MATERIALS.ID,
TB_MATERIALS.NAME,
TB_MATERIALS.TYPE,
TB_MATERIALS.THRESHOLD_QUANTITY
FROM
TB_MATERIALS 
)
UNION
(
SELECT
TB_OTHER_MATERIALS.ID,
TB_OTHER_MATERIALS.NAME,
TB_OTHER_MATERIALS.TYPE,
TB_OTHER_MATERIALS.THRESHOLD_QUANTITY
FROM
TB_OTHER_MATERIALS
)
GO
