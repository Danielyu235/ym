CREATE TABLE TB_ACCIDENT_SURVERY (
TYPE NVARCHAR(255) NULL ,--事故类型
HAPPEN_TIME NVARCHAR(255) NULL ,--事故发生时间
ADDRESS NVARCHAR(255) NULL ,--事故地点
SITE_PROFILE NVARCHAR(255) NULL ,--事故地点概况
SAFETY_REGULATION NVARCHAR(255) NULL ,--事故前安全监管情况
SAFE_RESCUE_SITUATION NVARCHAR(255) NULL ,--事故发生情况及抢救情况
LOSSES NVARCHAR(255) NULL ,--人员伤亡和直接经济损失
IMMEDIATE_CAUSE NVARCHAR(255) NULL ,--直接原因
INDIRECT_REASON NVARCHAR(255) NULL ,--间接原因
ACCDIENT_NATURE NVARCHAR(255) NULL ,--事故性质
RESPONSIBILITY NVARCHAR(255) NULL ,--责任划分与处理建议

