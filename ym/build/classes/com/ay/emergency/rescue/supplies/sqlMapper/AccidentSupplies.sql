CREATE TABLE TB_ACCIDENT_SUPPLIES (
AC_ID NVARCHAR(255) NULL ,--事故Id
MATERIAL_NAME NVARCHAR(255) NULL ,--物质名称
MODEL NVARCHAR(255) NULL ,--型号
UNIT NVARCHAR(255) NULL ,--单位
NUM NVARCHAR(255) NULL ,--数量
WAREHOUSE NVARCHAR(255) NULL ,--库号
REMARK NVARCHAR(255) NULL ,--备注

ID VARCHAR(50) NOT NULL PRIMARY KEY ,
STATUS CHAR(1) NULL DEFAULT ((1)) ,
CREATED VARCHAR(32) NULL ,
CREATE_TIME DATETIME NULL ,
UPDATED VARCHAR(32) NULL ,
UPDATE_TIME DATETIME NULL 
)