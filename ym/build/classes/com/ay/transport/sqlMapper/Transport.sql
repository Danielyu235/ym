CREATE TABLE TB_TRANSPORT (
CORP_KEY NVARCHAR(255) NULL ,--序号
DANGEROUS NVARCHAR(255) NULL ,--危险源
LOSS NVARCHAR(255) NULL ,--可能造成的伤害和损失
