CREATE TABLE TB_OTHER_MATERIALS (
NAME NVARCHAR(255) NULL ,--物质名称
TYPE NVARCHAR(255) NULL ,--类型
DESCRIPTION NVARCHAR(255) NULL ,--危险性分类及说明
THRESHOLD_QUANTITY NVARCHAR(255) NULL ,--临界量

ID VARCHAR(50) NOT NULL PRIMARY KEY ,
STATUS CHAR(1) NULL DEFAULT ((1)) ,
CREATED VARCHAR(32) NULL ,
CREATE_TIME DATETIME NULL ,
UPDATED VARCHAR(32) NULL ,
UPDATE_TIME DATETIME NULL 
)