CREATE TABLE TB_LOG_RESCUE (
P_ID NVARCHAR(255) NULL ,--预案Id
A_ID NVARCHAR(255) NULL ,--事故Id
C_ID NVARCHAR(255) NULL ,--指挥部Id
LEVEL NVARCHAR(255) NULL ,--响应等级

